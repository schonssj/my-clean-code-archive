namespace CleanCode.Names.ClassNaming
{
    public class Examples { }

    /// <summary>
    /// Classes should be named with substantives, such as the examples below.
    /// </summary>
    public class Customer { }
    public class WikiPage { }
    public class Account { }
    public class AddressParser { }

    /// <summary>
    /// Classes also are not supposed to be named with verbs.
    /// </summary>
    public class WriteFile { }
    
    /// <summary>
    /// We can improve the example above using a substantive and an adjective.
    /// </summary>
    public class FileWriter { }
}